<script>
    import $ from "jquery"
    import promptUtil from '../../utils/promptUtil'
    import staticUtil from '../../utils/staticUtil'
    import storageUtil from '../../utils/storageUtil'
    import '../../api/restfulapi'
    // 引入验证码组件
    import identify from './identify.vue'
    import {
        qs,
        indexForm,
        getTelAddress
    } from '../../api/api'


    export default {
        // 注册验证码组件
        components: {identify},
        data() {
            return {
                ruleForm: {
                    name: '',
                    phone: '',
                    city: '',
                    message: '',
                    verificationCode:''
                },
                rules: {
                    name: [
                        {required: true, message: '请输入姓名', trigger: 'blur'},
                    ],
                    phone: [
                        {required: true, message: '请输入手机号', trigger: 'blur'},
                    ],
                    city: [
                        {required: true, message: '请输入城市', trigger: 'blur'},
                    ],
                    message: [
                        {required: true, message: '请输入留言', trigger: 'blur'},
                    ],
                    verificationCode: [
                        {required: true, message: '请输入验证码', trigger: 'blur'},
                    ],

                },
                addressTitle: '',
                addressValue: '',
                telTitle: '',
                telValue: '',
                // 验证码初始值
                identifyCode: '1234',
                // 验证码的随机取值范围
                identifyCodes: '1234567890'
            }
        },
        mounted() {
            // 刷新页面就生成随机验证码
            this.identifyCode = ''
            this.makeCode(this.identifyCodes, 4)
            this.getAdressTelInformation()
        },
        methods: {
            // 点击验证码刷新验证码
            changeCode() {
                this.identifyCode = ''
                this.makeCode(this.identifyCodes, 4)
            },
            // 生成一个随机整数  randomNum(0, 10) 0 到 10 的随机整数， 包含 0 和 10
            randomNum(min, max) {
                max = max + 1
                return Math.floor(Math.random() * (max - min) + min)
            },
            // 随机生成验证码字符串
            makeCode(data, len) {
                for (let i = 0; i < len; i++) {
                    this.identifyCode += data[this.randomNum(0, data.length - 1)]
                }
                // console.log("this.identifyCode",this.identifyCode)
            },
            submitForm() {
                if (this.ruleForm.name == "" || this.ruleForm.phone == "" || this.ruleForm.city == "" || this.ruleForm.message == ""||this.ruleForm.verificationCode=="") {
                    promptUtil.warning(this, "请输入相关信息")
                    return
                }
                if(this.ruleForm.verificationCode&&this.ruleForm.verificationCode!=this.identifyCode){
                    promptUtil.warning(this,"请输入正确的验证码")
                    return
                }
                let phoneValdate = /^[1][3,4,5,7,8][0-9]{9}$/;
                if (!phoneValdate.test(this.ruleForm.phone)) {
                    promptUtil.warning(this, "手机号格式不正确");
                    return;
                }
                indexForm(qs.stringify({
                    name: this.ruleForm.name,
                    phone: this.ruleForm.phone,
                    city: this.ruleForm.city,
                    content: this.ruleForm.message,
                })).then(res => {
                    if (res.code == SUCCESS_CODE) {
                        promptUtil.success(this, res.msg)
                        this.ruleForm.name = ""
                        this.ruleForm.phone = ""
                        this.ruleForm.city = ""
                        this.ruleForm.message = ""
                        this.ruleForm.verificationCode = ""
                        this.identifyCode = ''
                        this.makeCode(this.identifyCodes, 4)
                        if (res.data && res.data != '[]') {
                        }
                    } else if (res.code == ERROR_CODE) {
                        promptUtil.error(this, res.msg)
                    } else {
                        promptUtil.wait(this)
                    }
                }).catch(err => {
                    promptUtil.LOG('JoinInUs-err', err)
                })
            },
            getAdressTelInformation() {
                getTelAddress(qs.stringify({
                    config_key: "tel,address"
                })).then(res => {
                    if (res.code == SUCCESS_CODE) {
                        if (res.data && res.data != '[]') {
                            this.addressTitle = res.data.address.title
                            this.addressValue = res.data.address.value
                            this.telTitle = res.data.tel.title
                            this.telValue = res.data.tel.value

                        }
                    } else if (res.code == ERROR_CODE) {
                        promptUtil.error(this, res.msg)
                    } else {
                        promptUtil.wait(this)
                    }
                })
            }
        }
    }
</script>

