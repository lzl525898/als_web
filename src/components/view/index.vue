        data() {
            return {
                addressTitle: '',
                addressValue: '',
                telTitle: '',
                telValue: '',
                bannerImgArray:[],
            };
        },
        mounted() {
            wheelPlanting();
            this.getAdressTelInformation()
            getBannerImage(qs.stringify({})).then(res=>{
                if (res.code==SUCCESS_CODE){
                    if (res.data&&res.data!='[]'){
                        // console.log("res",res)
                        res.data.forEach(item=>{
                            this.bannerImgArray.push(item.pic)
                            // console.log("this.bannerImgArray",this.bannerImgArray)
                        })
                    }
                } else if(res.code==ERROR_CODE){
                    promptUtil.error(this,res.msg)
                }else{
                    promptUtil.wait(this)
                }
            }).catch(err=>{
                promptUtil.LOG('getBannerImage-err',err)
            })
        },
        methods: {
            entrance() {
                // console.log('888')
                this.$router.push({path: "/login"});
            },
            getAdressTelInformation() {
                getTelAddress(qs.stringify({
                    config_key: "tel,address"
                })).then(res => {
                    if (res.code == SUCCESS_CODE) {
                        if (res.data && res.data != '[]') {
                            //console.log("res", res)
